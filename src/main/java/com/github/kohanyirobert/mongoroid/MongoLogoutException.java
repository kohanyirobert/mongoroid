package com.github.kohanyirobert.mongoroid;

// @checkstyle:off Javadoc
@SuppressWarnings("serial")
public final class MongoLogoutException extends MongoException {

  public MongoLogoutException() {
    super();
  }

  public MongoLogoutException(String message, Throwable cause) {
    super(message, cause);
  }

  public MongoLogoutException(String message) {
    super(message);
  }

  public MongoLogoutException(Throwable cause) {
    super(cause);
  }
}
